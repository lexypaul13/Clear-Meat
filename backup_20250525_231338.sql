CREATE OR REPLACE FUNCTION "public"."safe_truncate"("input_text" "text", "max_length" integer DEFAULT 100, "suffix" "text" DEFAULT '...'::"text") RETURNS "text"
    LANGUAGE "plpgsql" IMMUTABLE
    AS $$
BEGIN
    IF input_text IS NULL THEN
        RETURN NULL;
    END IF;
    
    IF length(input_text) <= max_length THEN
        RETURN input_text;
    ELSE
        RETURN left(input_text, max_length - length(suffix)) || suffix;
    END IF;
END;
$$;


ALTER FUNCTION "public"."safe_truncate"("input_text" "text", "max_length" integer, "suffix" "text") OWNER TO "postgres";


COMMENT ON FUNCTION "public"."safe_truncate"("input_text" "text", "max_length" integer, "suffix" "text") IS 'Efficiently truncates text fields to avoid octet_length case when operations in API responses.';



CREATE OR REPLACE FUNCTION "public"."update_updated_at_column"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    SET "search_path" TO 'public'
    AS $$
BEGIN
    -- Check if the table has updated_at column
    IF EXISTS (
        SELECT 1
        FROM information_schema.columns
        WHERE table_name = TG_TABLE_NAME
        AND column_name = 'updated_at'
    ) THEN
        NEW.updated_at = NOW();
    END IF;
    RETURN NEW;
END;
$$;


