CREATE OR REPLACE VIEW "public"."security_compliance_monitor" AS
 SELECT 'description_cache'::"text" AS "table_name",
    'RLS'::"text" AS "security_feature",
        CASE
            WHEN (EXISTS ( SELECT 1
               FROM "information_schema"."tables"
              WHERE ((("tables"."table_schema")::"name" = 'public'::"name") AND (("tables"."table_name")::"name" = 'description_cache'::"name")))) THEN
            CASE
                WHEN "rls"."rowsecurity" THEN 'ENABLED'::"text"
                ELSE 'DISABLED'::"text"
            END
            ELSE 'TABLE_NOT_EXISTS'::"text"
        END AS "status"
   FROM ( SELECT "c"."relrowsecurity" AS "rowsecurity"
           FROM ("pg_class" "c"
             LEFT JOIN "pg_namespace" "n" ON (("n"."oid" = "c"."relnamespace")))
          WHERE (("n"."nspname" = 'public'::"name") AND ("c"."relname" = 'description_cache'::"name"))
         LIMIT 1) "rls"
UNION ALL
 SELECT 'pg_trgm'::"text" AS "table_name",
    'Extension Schema'::"text" AS "security_feature",
        CASE
            WHEN ("n"."nspname" = 'extensions'::"name") THEN 'COMPLIANT'::"text"
            WHEN ("n"."nspname" = 'public'::"name") THEN 'NON_COMPLIANT'::"text"
            ELSE 'EXTENSION_NOT_EXISTS'::"text"
        END AS "status"
   FROM ("pg_extension" "e"
     LEFT JOIN "pg_namespace" "n" ON (("n"."oid" = "e"."extnamespace")))
  WHERE ("e"."extname" = 'pg_trgm'::"name");


